import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

const questsDir = path.join(rootDir, "Quests");
const informationDir = path.join(rootDir, "Information");
const questsDataFile = path.join(rootDir, "src", "QuestsData.ts");
const informationDataFile = path.join(rootDir, "src", "InformationData.ts");

function toIdentifier(value) {
  const cleaned = value.replace(/[^A-Za-z0-9_$]/g, "_");
  return /^[A-Za-z_$]/.test(cleaned) ? cleaned : `_${cleaned}`;
}

function normalizePath(value) {
  return value.split(path.sep).join("/");
}

function toExportKey(relativePathWithoutExtension) {
  return relativePathWithoutExtension.replace(/,/g, "-");
}

async function collectJsonFiles(dir, parentRelative = "") {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const relativePath = parentRelative ? path.join(parentRelative, entry.name) : entry.name;
    const fullPath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      const nestedFiles = await collectJsonFiles(fullPath, relativePath);
      files.push(...nestedFiles);
      continue;
    }

    if (!entry.isFile()) {
      continue;
    }

    if (path.extname(entry.name).toLowerCase() !== ".json") {
      continue;
    }

    files.push(relativePath);
  }

  return files;
}

function buildDataModule({ exportName, importRoot, files }) {
  const imports = [];
  const entries = [];
  const usedAliases = new Set();
  const usedKeys = new Set();

  for (const relativeFilePath of files) {
    const normalizedPath = normalizePath(relativeFilePath);
    const relativeWithoutExt = normalizedPath.replace(/\.json$/i, "");
    const alias = toIdentifier(`${exportName}_${relativeWithoutExt}`);
    const key = toExportKey(relativeWithoutExt);

    if (usedAliases.has(alias)) {
      throw new Error(`Duplicate generated import alias "${alias}" in ${exportName} data.`);
    }
    usedAliases.add(alias);

    if (usedKeys.has(key)) {
      throw new Error(`Duplicate generated export key "${key}" in ${exportName} data.`);
    }
    usedKeys.add(key);

    imports.push(`import ${alias} from '${importRoot}/${normalizedPath}' with { type: "json" };`);
    entries.push(`  '${key}': ${alias},`);
  }

  const header = [
    "// This file is auto-generated by scripts/generate-json-exports.mjs.",
    "// Do not edit this file manually.",
    ""
  ].join("\n");

  return `${header}${imports.join("\n")}\n\nexport const ${exportName} = {\n${entries.join("\n")}\n} as const;\n`;
}

async function main() {
  const [questFiles, infoFiles] = await Promise.all([
    collectJsonFiles(questsDir),
    collectJsonFiles(informationDir),
  ]);

  const sortedQuestFiles = questFiles.sort((a, b) => a.localeCompare(b));
  const sortedInfoFiles = infoFiles.sort((a, b) => a.localeCompare(b));

  if (sortedQuestFiles.length === 0) {
    throw new Error(`No JSON files found in ${questsDir}`);
  }

  if (sortedInfoFiles.length === 0) {
    throw new Error(`No JSON files found in ${informationDir}`);
  }

  const questsSource = buildDataModule({
    exportName: "Quests",
    importRoot: "../Quests",
    files: sortedQuestFiles,
  });
  const informationSource = buildDataModule({
    exportName: "Information",
    importRoot: "../Information",
    files: sortedInfoFiles,
  });

  await Promise.all([
    fs.writeFile(questsDataFile, questsSource, "utf8"),
    fs.writeFile(informationDataFile, informationSource, "utf8"),
  ]);

  console.log(
    `Updated ${questsDataFile} (${sortedQuestFiles.length} files) and ${informationDataFile} (${sortedInfoFiles.length} files).`,
  );
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
